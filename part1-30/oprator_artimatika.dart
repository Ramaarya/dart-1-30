void main(List<String> args) {
  var first = 10;
  var second = 3;

  print(first + second); // penambahan
  print(first - second); // pengurangan
  print(first / second); // pembagian hasil bentuk double
  print(first ~/ second); // pembagian hasil bentuk integer
  print(first % second); // sisa pembagian
}
